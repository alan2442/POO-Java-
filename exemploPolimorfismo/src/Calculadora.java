public class Calculadora {
    //Metodo sobrecarregado para somar dois números inteiros
    public int somar(int a, int b) {
        return a + b;
    }

    //Metodo sobrecarregado para três números inteiros
    public int somar(int a, int b, int c) {
        return a + b + c;
    }

    //Metodo sobrecarregado para somar dois números pontos flutuantes
    public double somar(double a, double b) {
        return a + b;
    }
}
